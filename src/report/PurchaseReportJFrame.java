    private void cmd_reportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_reportActionPerformed
        // TODO add your handling code here:
        String s_period = Select_Period_ComboBox.getSelectedItem().toString();
        Date start_date = Start_DateChooser.getDate();
        Date end_date = End_DateChooser.getDate();

        if (start_date.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter start date!!");
        } else if (end_date.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter end date!!");
        } else {
            try {
                java.sql.Date s_date = new java.sql.Date(start_date.getTime());
                java.sql.Date e_date = new java.sql.Date(end_date.getTime());
                Map<String, Object> map = new HashMap<String, Object>();
                map.put("start_date", s_date);
                map.put("end_date", e_date);
                
                JasperDesign jd = JRXmlLoader.load("E:\\AMS\\AccountManagementSystem\\src\\report\\PurchaseReport.jrxml");
                JasperReport jr = JasperCompileManager.compileReport(jd);
                JasperPrint jp = JasperFillManager.fillReport(jr, map, conn);
                JasperViewer.viewReport(jp);
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }


    }//GEN-LAST:event_cmd_reportActionPerformed

    private void cmd_viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_viewActionPerformed
        // TODO add your handling code here:
        String s_date = Start_DateChooser.getDateFormatString();
        String e_date = End_DateChooser.getDateFormatString();
        try {
            String sql = "SELECT date as 'Date', vendor_name as 'Vendor Name', product_name as 'Product Name', "
                    + "quantity as 'Quantity', unit_price as 'Unit Price', total as 'Total' "
                    + "FROM purchases WHERE date BETWEEN '"+s_date+"' AND '"+e_date+"' AND debit IS NOT NULL ";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            purchase_report_view_table.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_cmd_viewActionPerformed

