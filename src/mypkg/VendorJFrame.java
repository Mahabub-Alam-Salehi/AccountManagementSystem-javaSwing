    public void CountryFillCombo()
    {
        String[] locales = Locale.getISOCountries();
        for(String countryCode : locales)
        {
            Locale obj = new Locale("", countryCode);
            //JOptionPane.showMessageDialog(null, obj.getDisplayCountry());
            String c_name = String.valueOf(obj.getDisplayCountry());
            country_combo.addItem(c_name);
            
        }
       
    }
        
   
