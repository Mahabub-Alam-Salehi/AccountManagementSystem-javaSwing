    private void cmd_save_vendorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_save_vendorActionPerformed
        // TODO add your handling code here:
        String v_name = txt_vendor_name.getText();
        String v_address = txt_vendor_address.getText();
        String city = txt_city.getText();
        String state = txt_state.getText();
        String zipcode = txt_zipcode.getText();
        String country = country_combo.getSelectedItem().toString();
        String home_phone = txt_home_phone.getText();
        String cell_phone = txt_cell_phone.getText();
        String email = txt_email.getText();
        
        
        if(v_name.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter vendor name");
        }
        else if(v_address.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter vendor address");
        }
        else if(city.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter City name");
        }
        else if(state.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter state name");
        }
        else if(zipcode.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter Zipcode");
        }
        else if(country.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please select country name");
        }
        else if(cell_phone.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter phone number");
        }
        else if(email.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter email");
        }
       
        else
        {
            try
            {
                String sql = "INSERT INTO vendor(vendor_name,vendor_address,city,state,"
                        + "zipcode,country,home_phone,cell_phone,email,alter_email) "
                        + "VALUES(?,?,?,?,?,?,?,?,?,?)";
                pst = conn.prepareStatement(sql);
                
                pst.setString(1, txt_vendor_name.getText());
                pst.setString(2, txt_vendor_address.getText());
                pst.setString(3, txt_city.getText());
                pst.setString(4, txt_state.getText());
                pst.setString(5, txt_zipcode.getText());
                pst.setString(6, country_combo.getSelectedItem().toString());
                pst.setString(7, txt_home_phone.getText());
                pst.setString(8, txt_cell_phone.getText());
                pst.setString(9, txt_email.getText());
                pst.setString(10, txt_alter_email.getText());
                pst.execute();
                
                JOptionPane.showMessageDialog(null, "Save");
                
                //close frame
                close();
                //call sales frame
                PurchaseJFrame pf = new PurchaseJFrame();
                pf.VendorFillCombo();
               // sf.setVisible(true);
              
                
            }catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
             
        
    }//GEN-LAST:event_cmd_save_vendorActionPerformed
} 
